import { randomBytes } from "node:crypto";
import { existsSync, writeFileSync } from "node:fs";
import readline from "node:readline/promises";
import process from "node:process";

import {
  buildDatabaseUrl,
  DEFAULT_POSTGRES_IMAGE,
  DEFAULT_POSTGRES_PORT,
  DEFAULT_POSTGRES_SCHEMA,
  DEFAULT_POSTGRES_USER,
  ENV_LOCAL_PATH,
  formatContainerStatus,
  getAvailableRuntimes,
  getDefaultContainerName,
  quoteEnvValue,
  runCommand,
} from "./local-postgres-shared.mjs";

const main = async () => {
  const generatedPassword = randomBytes(18).toString("base64url");
  const defaultContainerName = getDefaultContainerName();
  const availableRuntimes = getAvailableRuntimes();

  if (availableRuntimes.length === 0) {
    console.error(
      "No supported container runtime found. Install Docker or Podman first.",
    );
    process.exit(1);
  }

  const rl = readline.createInterface({
    input: process.stdin,
    output: process.stdout,
  });
  const nonInteractive = process.env.LOCAL_POSTGRES_NON_INTERACTIVE === "1";

  const ask = async (label, fallback, validate) => {
    if (nonInteractive) {
      const value = fallback;
      const validationMessage = validate ? validate(value) : null;

      if (validationMessage) {
        throw new Error(validationMessage);
      }

      console.log(`${label}: ${value}`);
      return value;
    }

    const prompt = fallback ? `${label} [${fallback}]: ` : `${label}: `;

    while (true) {
      const answer = (await rl.question(prompt)).trim();
      const value = answer || fallback;
      const validationMessage = validate ? validate(value) : null;

      if (!validationMessage) {
        return value;
      }

      console.log(validationMessage);
    }
  };

  const askYesNo = async (label, defaultValue = true) => {
    if (nonInteractive) {
      console.log(`${label}: ${defaultValue ? "yes" : "no"}`);
      return defaultValue;
    }

    const suffix = defaultValue ? "Y/n" : "y/N";
    const answer = (await rl.question(`${label} [${suffix}]: `))
      .trim()
      .toLowerCase();

    if (!answer) {
      return defaultValue;
    }

    return ["y", "yes"].includes(answer);
  };

  try {
  console.log("Local PostgreSQL setup");
  console.log("======================");
  console.log(`Available runtimes: ${availableRuntimes.join(", ")}`);

  const runtime = await ask(
    "Choose a container runtime",
    availableRuntimes[0],
    (value) =>
      availableRuntimes.includes(value)
        ? null
        : `Choose one of: ${availableRuntimes.join(", ")}`,
  );

  const port = await ask(
    "Host port to expose PostgreSQL on",
    process.env.LOCAL_POSTGRES_PORT ?? DEFAULT_POSTGRES_PORT,
    (value) => {
      const portNumber = Number.parseInt(value, 10);
      return Number.isInteger(portNumber) && portNumber > 0 && portNumber < 65536
        ? null
        : "Enter a valid TCP port between 1 and 65535.";
    },
  );

  const dbName = await ask(
    "Database name",
    process.env.LOCAL_POSTGRES_DB ?? "app_local",
    (value) => (value ? null : "Database name cannot be empty."),
  );

  console.log(`Strong password suggestion: ${generatedPassword}`);
  const password = await ask(
    "Database password",
    process.env.LOCAL_POSTGRES_PASSWORD ?? generatedPassword,
    (value) => (value && value.length >= 8 ? null : "Use at least 8 characters."),
  );

  const schema = await ask(
    "Schema name",
    process.env.LOCAL_POSTGRES_SCHEMA ?? DEFAULT_POSTGRES_SCHEMA,
    (value) => (value ? null : "Schema name cannot be empty."),
  );

  const containerName = await ask(
    "Container name",
    process.env.LOCAL_POSTGRES_CONTAINER_NAME ?? defaultContainerName,
    (value) => (value ? null : "Container name cannot be empty."),
  );

  if (existsSync(ENV_LOCAL_PATH)) {
    const overwrite = await askYesNo(".env.local already exists. Overwrite it?", false);
    if (!overwrite) {
      console.log("Cancelled without changing .env.local.");
      process.exit(0);
    }
  }

  const existingContainer = formatContainerStatus(runtime, containerName);
  if (existingContainer) {
    const replace = await askYesNo(
      `Container ${containerName} already exists. Replace it?`,
      false,
    );

    if (!replace) {
      console.log("Cancelled without replacing the existing container.");
      process.exit(0);
    }

    const removeResult = runCommand(runtime, ["rm", "-f", containerName]);
    if (removeResult.status !== 0) {
      console.error(`Unable to remove existing container ${containerName}.`);
      process.exit(removeResult.status ?? 1);
    }
  }

  const databaseUrl = buildDatabaseUrl({
    dbName,
    password,
    port,
    schema,
  });

  const envFile = `# Generated by pnpm local:postgres:setup
LOCAL_POSTGRES_RUNTIME=${quoteEnvValue(runtime)}
LOCAL_POSTGRES_CONTAINER_NAME=${quoteEnvValue(containerName)}
POSTGRES_HOST="localhost"
POSTGRES_PORT=${quoteEnvValue(port)}
POSTGRES_DB=${quoteEnvValue(dbName)}
POSTGRES_USER=${quoteEnvValue(DEFAULT_POSTGRES_USER)}
POSTGRES_PASSWORD=${quoteEnvValue(password)}
POSTGRES_SCHEMA=${quoteEnvValue(schema)}
DATABASE_URL=${quoteEnvValue(databaseUrl)}
`;

  const skipRun = process.env.LOCAL_POSTGRES_SKIP_RUN === "1";
  if (!skipRun) {
    const runArgs = [
      "run",
      "-d",
      "--name",
      containerName,
      "-e",
      `POSTGRES_DB=${dbName}`,
      "-e",
      `POSTGRES_USER=${DEFAULT_POSTGRES_USER}`,
      "-e",
      `POSTGRES_PASSWORD=${password}`,
      "-p",
      `${port}:5432`,
      DEFAULT_POSTGRES_IMAGE,
    ];

    const runResult = runCommand(runtime, runArgs);
    if (runResult.status !== 0) {
      console.error("PostgreSQL container creation failed.");
      process.exit(runResult.status ?? 1);
    }
  }

  writeFileSync(ENV_LOCAL_PATH, envFile, "utf8");

  console.log("");
  console.log(skipRun ? "Dry run completed." : "Local PostgreSQL container created.");
  console.log(`.env.local written to ${ENV_LOCAL_PATH}`);
  console.log(`Runtime: ${runtime}`);
  console.log(`Container: ${containerName}`);
  console.log(`Port: ${port}`);
  console.log(`Database: ${dbName}`);
  console.log(`Schema: ${schema}`);
  console.log("");
  console.log("Useful commands:");
  console.log("- pnpm local:postgres:start");
  console.log("- pnpm local:postgres:stop");
  console.log("- pnpm local:postgres:status");
  console.log("- pnpm local:postgres:logs");
  console.log("- pnpm local:postgres:remove");
  } finally {
    rl.close();
  }
};

main().catch((error) => {
  const message =
    error instanceof Error ? error.message : "Local PostgreSQL setup failed.";
  console.error(message);
  process.exit(1);
});
